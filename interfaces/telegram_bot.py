def render_latex_to_image(latex: str) -> io.BytesIO:
    """–†–µ–Ω–¥–µ—Ä–∏—Ç LaTeX-–≤—ã—Ä–∞–∂–µ–Ω–∏–µ –≤ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ."""
    logger.debug(f"–ù–∞—á–∞–ª–æ —Ä–µ–Ω–¥–µ—Ä–∏–Ω–≥–∞ LaTeX: {latex}")
    try:
        plt.figure(figsize=(10, 1))
        plt.text(0.5, 0.5, f"${latex}$", size=14, ha='center', va='center')
        plt.axis('off')
        
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ –±—É—Ñ–µ—Ä
        buf = io.BytesIO()
        plt.savefig(buf, format='png', bbox_inches='tight', dpi=300)
        plt.close()
        
        buf.seek(0)
        logger.debug("LaTeX —É—Å–ø–µ—à–Ω–æ –æ—Ç—Ä–µ–Ω–¥–µ—Ä–µ–Ω")
        return buf
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Ä–µ–Ω–¥–µ—Ä–∏–Ω–≥–µ LaTeX: {e}")
        raise

